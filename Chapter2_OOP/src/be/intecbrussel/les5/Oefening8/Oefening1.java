package be.intecbrussel.les5.Oefening8;

public class Oefening1 {
    public static void main(String[] args) {
        int numberInt = 25;
        double numberInDouble = 2.99;
        char notANumber = 'j';
        Integer intObject = numberInt;
        Double doubleObj = numberInDouble;
        Character charObj = notANumber;
        System.out.println("Conversion of int primitive data type to Integer Wrapper class  "+intObject);
        System.out.println("Conversion of double primitive data type to Double Wrapper class  "+ doubleObj);
        System.out.println("Conversion of char primitive data type to Character Wrapper class  "+charObj);


    }
}
