package be.intecbrussel.les1;

public class FloatExample {
    public static void main(String[] args) {
        float a = 10000f;
        float b = 200000F;
        float c =  a + b;
        System.out.println("Value of a is " + a);
        System.out.println("Value of b is " + b);
        System.out.println("Value of c is " + c);
    }
}
