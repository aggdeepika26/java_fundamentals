package be.intecbrussel.les5.Oefeningen4;

public class Oefning3 {
    public static void main(String[] args) {
        int age = 0;
        String name = "Deepika";
        System.out.println("My name is " +name);
        do {
            age++;
        }
        while(age!= 42);
        System.out.println("My age is " +age + "years");


    }
}
