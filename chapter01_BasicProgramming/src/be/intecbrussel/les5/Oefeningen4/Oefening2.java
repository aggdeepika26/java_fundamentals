//Program to print the numbers from 100 to 50 in reverse order using while
package be.intecbrussel.les5.Oefeningen4;

public class Oefening2 {
    public static void main(String[] args) {
        System.out.println("The numbers from 100 to 50 in reverse order are ");
        int i = 100;
        while(i>=50)
        {
            System.out.println(i);
            i--;
        }
    }
}
